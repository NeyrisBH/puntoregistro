    private void registrarDatos() {
        DatosDaoImpl d = new DatosDaoImpl();
        DatosUsuario datos = new DatosUsuario();
        DatosUsuario dato = d.getDatosUsuarios(txtUsuarios.getText());

        datos.setNombres(txtNombres.getText().trim().strip());
        datos.setApellidos(txtApellidos.getText().trim().strip());
        datos.setEmail(txtEmails.getText().trim().strip());
        datos.setUsuario(txtUsuarios.getText().trim().strip());
        datos.setContraseña(txtContraseña.getText().trim().strip());

        if (txtNombres.getText().length() == 0 || txtApellidos.getText().length() == 0 || txtEmails.getText().length() == 0
                || txtUsuarios.getText().length() == 0 || txtContraseña.getText().length() == 0)
            JOptionPane.showMessageDialog(this, "Llene el formulario completo");
        else {
            if (null != dato.getUsuario()) 
                JOptionPane.showMessageDialog(this, "El Usuario ya esta registrado en el sistema.\n");
            else {
                if (txtContraseña.getText().trim().strip().equals(txtConfContraseña.getText().trim().strip())) {
                    d.insertarDatosUsuarios(datos);
                    limpiarRegistrar();
                } else
                    JOptionPane.showMessageDialog(this, "Las contraseñas no coinciden");
            }
        }
    }

    private void limpiarRegistrar() {
        txtNombres.setText("");
        txtApellidos.setText("");
        txtEmails.setText("");
        txtUsuarios.setText("");
        txtContraseña.setText("");
        txtConfContraseña.setText("");
    }

